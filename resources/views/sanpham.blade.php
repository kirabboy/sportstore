                <li style="text-align:justify">Gập người xuống tay chạm mũi chân, thực hiện từng bên trong 10 giây.</li>
                <li style="text-align:justify">Đứng thẳng đưa gót chân phải lên phía sau, tay phải giữ nó trong 10 giây. Lặp lại và đổi bên với chân trái.</li>
                <li style="text-align:justify">Bật nhảy sang 2 bên đồng thời giơ tay lên cao, trở lại vị trí ban đầu trong 2 phút.</li>
                <li style="text-align:justify">Squat đúng tư thế 10- 15 lần.</li>
            </ul>
            <h3 style="text-align:justify"><strong>4.2. Duy trì nhịp tim mục tiêu trong suốt buổi tập luyện</strong></h3>
            <p style="text-align:justify">Nhịp tim đóng vai trò quan trọng, đặc biệt với những người muốn loại bỏ mỡ thừa. Bạn cần duy trì trong giới hạn từ 50- 85% nhịp tim tối đa của người bình thường. Nói là ngưỡng giới hạn sức chịu đựng của cơ thể.</p>
            <p style="text-align:center " class="card-img"><img src="https://lifesport.vn/uploads/upload/images/theo-doi-qua-trinh-luyen-tap-bang-app.jpg"  class="card-img" style="width:100%" ></p>
            <p style="text-align:justify">Công thức tính nhịp tim mục tiêu như sau:</p>
            <ul>
                <li style="text-align:justify">Giới hạn dưới =&nbsp; (220 – tuổi của bạn hiện tại) * 0,5.</li>
                <li style="text-align:justify">Giới hạn trên = (220 – tuổi của bạn hiện tại) * 0,85</li>
            </ul>
            <p style="text-align:justify">Ví dụ bạn năm nay 25 tuổi, áp dụng công thức, ta có nhịp tim mục tiêu của bạn nằm trong khoảng:</p>
            <ul>
                <li style="text-align:justify">Giới hạn dưới = (220 – 25) * 0,5 = 97,5.</li>
                <li style="text-align:justify">Giới hạn trên = (220 – 25) * 0,85 = 165,75</li>
            </ul>
            <p style="text-align:justify">Như vậy bạn cần duy trì nhịp tim mục tiêu trong khoảng 97,5- 165,75 suốt cả buổi tập luyện. Nó giúp đốt cháy mỡ thừa nhiều nhất và phù hợp với độ tuổi của bạn. Để đạt được điều đó, bạn phải khởi động thật kỹ trước khi chạy bộ.</p>
            <p style="text-align:justify">Thêm vào đó, bạn nên chạy bộ giữ nhịp tim mục tiêu trong khoảng 30- 60 phút. Tần suất tập luyện từ 3- 5 ngày là phù hợp. Cơ thể cần thời gian nghỉ ngơi, phục hồi.&nbsp;</p>
            <p style="text-align:justify">Nếu bạn thấy nhàm chán với các bài tập thông thường, hãy thử chạy bộ trên độ nghiêng của máy. Nó mô phỏng động tác chạy lên dốc. Tin rằng bạn sẽ không còn thấy nhàm chán và đốt chát lượng lớn Calo.</p>
            <h3 style="text-align:justify"><strong>4.3. Điều chỉnh tốc độ phù hợp với mục đích và đối tượng sử dụng</strong></h3>
            <p style="text-align:justify">Tùy vào mục đích sử dụng và đối tượng dùng, bạn cần điều chỉnh tốc độ của máy chạy bộ cho phù hợp.&nbsp;</p>
            <p style="text-align: center;"><img alt="de-dang-dieu-chinh-toc-do-chay" longdesc="Bạn cần điều chỉnh tốc độ của máy chạy bộ cho phù hợp"  class="card-img" src="https://lifesport.vn/uploads/upload/images/de-dang-dieu-chinh-toc-do-chay.jpg" style="width:100%" title="Bạn cần điều chỉnh tốc độ của máy chạy bộ cho phù hợp"></p>
            <h4 style="text-align:justify"><strong>4.3.1.&nbsp; Quy trình điều chỉnh tốc độ chung</strong></h4>

            <p style="text-align:justify">Khi mới bật máy, bạn cho cơ thể làm quen bằng cách đi bộ trong 1- 1,5 phút với tốc độ 3km/h. Mắt bạn nhìn thẳng, hít thở đều. Sau đó bạn mới tăng tốc dần và chạy, khoảng 8km/h hoặc hơn phụ thuộc vào điều kiện sức khỏe của người tập.</p>
            <p style="text-align:justify">Theo hiệp hội thể dục tại Mỹ, 1 người có cân nặng 70 kg có thể đốt cháy 97 Calo nếu chạy bộ 10 phút với tốc độ 8km/h. Nếu tăng lên 9km/h, người đó có thể đốt cháy 123 Calo. Dễ dàng nhận thấy chỉ với hoạt động đơn giản này, bạn đã tiêu thụ khá nhiều năng lượng.</p>
            <p style="text-align:justify">Khi tập luyện đủ thời gian, bạn bắt đầu thấy đuối sức và muốn dừng lại. Khi đó, đừng vội tắt máy ngay, bạn nên tiếp tục điều chỉnh về mức 3km/h và đi bộ trong khoảng 2 phút. Nó giúp điều hòa lại nhịp tim, thư giãn cơ bắp.</p>
            <h4 style="text-align:justify"><strong>4.3.2. Tốc độ phù hợp với các đối tượng</strong></h4>
            <p style="text-align:justify">Mỗi đối tượng khác nhau sẽ có những mức tốc độ tương ứng. Do vậy, bạn có thể tham khảo thông tin sau:</p>
            <ul>
                <li style="text-align:justify">Người có thể trạng yếu, người lớn tuổi, người mắc bệnh xương khớp: 1- 3 km/h.</li>
                <li style="text-align:justify">Người mới bắt đầu chạy bộ, trong giai đoạn làm quen: 4.5- 6 km/h.</li>
                <li style="text-align:justify">Người có thể trạng bình thường, sức khỏe tốt: 7.5- 9 km/h.</li>
                <li style="text-align:justify">Người chạy bộ lâu năm, chuyên nghiệp: 14.5 - 18 km/h.</li>
            </ul>
            <h4 style="text-align:justify"><strong>4.3.3. Nâng cao hiệu quả với vòng quay chân cao</strong></h4>
            <p style="text-align:justify">Tác giả của cuốn sách “Đào tạo 3 môn phối hợp” Joe Friel nói rằng người tập hiệu quả khi có vòng quay chân cao. Nghĩa là bạn phải chạy bộ trong khoảng 160- 180 nhịp/ phút. Để xác định điều đó, bạn chỉ cần đếm tần suất chân chạm thảm trong 30 giây, nhân 2 lần sẽ ra kết quả.</p>
            <p style="text-align:justify">Chính vì vậy, bạn nên cải thiện số sải chân khi chạy bằng cách tập trung vào những bước ngắn, nhanh. Bạn nên giữ chân vung thấp dưới eo kết hợp cùng tinh thần lạc quan vui vẻ. Quá trình tập luyện sẽ diễn ra nhanh chóng và mang đến nguồn năng lượng tích cực cho bạn đấy!</p>
            <h3 style="text-align:justify"><strong>4.4. Thời điểm tập máy chạy bộ tốt nhất trong ngày</strong></h3>
            <p style="text-align:justify">Nhiều người băn khoăn vậy thời điểm nào chạy bộ tốt nhất trong ngày? Lời khuyên là bạn nên tập vào buổi sáng, hạn chế thời điểm buổi tối trước khi đi ngủ sẽ gây khó vào giấc. Nó còn tùy thuộc vào thời gian rảnh của cá nhân người tập.</p>
            <p style="text-align:justify">Do đó, bạn có thể sử dụng máy chạy bộ tất cả các thời điểm trong ngày, tránh khi ăn no hoặc sát giờ đi ngủ. Bạn nên tạo lập thói quen và cố gắng kiên trì để có sức khỏe tốt, cơ thể dẻo dai, vóc dáng thon gọn.</p>
            <h3 style="text-align:justify"><strong>4.5. Lưu ý khi sử dụng máy chạy bộ với các độ tuổi</strong></h3>
            <p style="text-align:justify">Đặc biệt mỗi độ tuổi cần nắm vững những lưu ý sau đây. Nó giúp nâng cao hiệu quả tập luyện, hạn chế chấn thương.&nbsp;</p>
            <p style="text-align: center;"><img alt="dung-may-chay-bo-voi-thoi-gian-30-phut"  class="card-img" longdesc="Người trưởng thành nên chạy bộ mỗi ngày với cường độ phù hợp trong 30 phút" src="https://lifesport.vn/uploads/upload/images/dung-may-chay-bo-voi-thoi-gian-30-phut.jpg" style="width:100%" title="Người trưởng thành nên chạy bộ mỗi ngày với cường độ phù hợp trong 30 phút"></p>
            <ul>
                <li style="text-align:justify">Trẻ em dưới 10 tuổi: Nhóm tuổi này có nguy cơ mất an toàn khi sử dụng máy chạy bộ. Các bé chỉ nên chạy chậm dưới sự giám sát của người lớn.</li>
                <li style="text-align:justify">Thanh thiếu niên: Nhóm tuổi này nên tập 30 phút mỗi ngày với tốc độ từ 2,5- 4,8 km/h.</li>
                <li style="text-align:justify">Người lớn có sức khỏe tốt: Nhóm đối tượng này nên chạy bộ mỗi ngày với cường độ phù hợp trong 30 phút. Thông thường khoảng 6- 8 km/h, cường độ cao trên 8km/h chỉ nên tập 3 buổi mỗi tuần.</li>
                <li style="text-align:justify">Người cao tuổi: Nhóm tuổi này thường mắc bệnh loãng xương hoặc đau nhức xương khớp. Do đó, họ chỉ nên đi bộ nhẹ nhàng hoặc chạy bộ cường độ thấp.</li>
            </ul>
            <p style="text-align:justify">Ngoài ra, khi chạy bộ mọi người đều nhớ rõ những lưu ý như sau:</p>
            <ul>
                <li style="text-align:justify">Bạn cần giữ tư thế chạy bộ chuẩn kỹ thuật: lưng thẳng, mắt hướng về phía trước, không nhìn xuống chân, đánh tay theo nhịp.</li>
                <li style="text-align:justify">Khi thực hiện chạy bộ, bạn nên đứng ở vị trí giữa, tránh bước hụt về sau.</li>
                <li style="text-align:justify">Nên ăn nhẹ trước khi tập từ 30- 60 phút.</li>
                <li style="text-align:justify">Bổ sung đầy đủ nước trong quá trình tập luyện, nên uống từng ngụm nhỏ.</li>
                <li style="text-align:justify">Đừng quên bật bản nhạc yêu thích giúp chạy bộ vui vẻ hơn.</li>
            </ul>
